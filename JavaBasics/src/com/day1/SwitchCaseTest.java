package com.day1;

public class SwitchCaseTest {
  public static void main(String[] args) {
    int num = 2;
    switch (num) {
      case 0:
        System.out.println("zero");
        break;
      case 1:
        System.out.println("one");
        break;
      default:
        System.out.println("Other");
    }
  }
}
