package com.day1;

/*
对于整数，有四种表示方式：
1. 二进制： 以0b开头
2.十进制
3. 八进制，以0开头
4. 十六进制，以0x开头

java中二进制符号位0表示正数，1表示负数，
一个正数，除符号位之外，各位取反，然后+1，就得到它对应的负数
 */
public class BinaryTest {
    public static void main(String[] args) {
        int num1 = 0b110;
        int num2 = 110;
        int num3 = 012;
        int num4 = 0x110a;

    }
}
